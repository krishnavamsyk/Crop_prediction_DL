    # Function to fetch weather forecast from OpenWeatherMap API
    def get_weather_forecast(lat, lon):
        # API_KEY = '7bd12cbbf7283b1b6d3ae9f67c201bdf'  
        API_KEY = st.secrets["OPENWEATHER_API_KEY"] # Replace with your OpenWeatherMap API key
        URL = f"http://api.openweathermap.org/data/2.5/forecast?lat={lat}&lon={lon}&appid={API_KEY}&units=metric"
        response = requests.get(URL)
        if response.status_code == 200:
            data = response.json()
            forecast_data = []
            for forecast in data.get('list', []):  # Use .get() to handle missing 'list'
                date_time = datetime.fromtimestamp(forecast['dt'])
                temp = forecast['main']['temp']
                humidity = forecast['main']['humidity']
                precipitation = forecast.get('rain', {}).get('3h', 0)
                windspeed = forecast['wind']['speed']
                forecast_data.append({
                    'datetime': date_time,
                    'temperature': temp,
                    'humidity': humidity,
                    'precipitation': precipitation,
                    'windspeed': windspeed
                })
            return pd.DataFrame(forecast_data)
        else:
            st.error("Failed to fetch weather data.")
            return pd.DataFrame()


